package com.qiang.qiangmp.bean;

/**
 * @author xiaoqiang
 * @date 19-3-11
 * 歌单类
 */
public class SongList {
    private String id;
    private String name;
    private String pic;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPic() {
        return pic;
    }

    public void setPic(String pic) {
        this.pic = pic;
    }
}
